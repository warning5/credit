package com.bluecloud.mvc.exception;

/**
 * @author Leo
 *
 */
public class HtmlFragmentDispatcherException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3458912444134128354L;

	/**
	 * 
	 */
	public HtmlFragmentDispatcherException() {
	}

	/**
	 * @param arg0
	 */
	public HtmlFragmentDispatcherException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public HtmlFragmentDispatcherException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public HtmlFragmentDispatcherException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
