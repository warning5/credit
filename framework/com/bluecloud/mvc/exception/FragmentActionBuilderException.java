package com.bluecloud.mvc.exception;

/**
 * @author Hanlu
 *
 */
public class FragmentActionBuilderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5986903585076653138L;

	/**
	 * 
	 */
	public FragmentActionBuilderException() {
		
	}

	/**
	 * @param arg0
	 */
	public FragmentActionBuilderException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public FragmentActionBuilderException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public FragmentActionBuilderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
