package com.bluecloud.mvc.exception;

/**
 * @author Hanlu
 *
 */
public class FragmentBeanBuilderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3906594900382791920L;

	/**
	 * 
	 */
	public FragmentBeanBuilderException() {
	}

	/**
	 * @param arg0
	 */
	public FragmentBeanBuilderException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public FragmentBeanBuilderException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public FragmentBeanBuilderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
